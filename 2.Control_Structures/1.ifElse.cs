using System;

namespace Control_Structures
{
    class IfElse
    {
        public static void Run()
        {
            int a = 5;
            if (a > 3)
            {
                Console.WriteLine("a is greater than 3");
            }
            else
            {
                Console.WriteLine("a is less than or equal to 3");
            }
        }
    }
}